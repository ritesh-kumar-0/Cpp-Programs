// Updation of Variables 

#include<iostream>
using namespace std;

int main(){
    int x = 5;   // Variable declared and initialized with 5
    cout << x << endl;
    x = x + 7;    //Value  update from 5 to 12
    cout <<x<<endl;
    x = x - 12;  // x = 0
    cout << x <<endl;

    return 0;
    
}