// Arithmetic Operators
#include<iostream>
using namespace std;

int main(){
    int x = 5, y = 2;

    cout<< x+y << endl;   // output = 7
    cout<< x-y << endl;   // output = 3
    cout<< x*y << endl;   // output =10
    cout<< x/y << endl;  // 2.5 but output be 2 ( here decimal part ignored   )
}